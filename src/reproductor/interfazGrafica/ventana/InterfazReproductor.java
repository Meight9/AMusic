    private int getIndex(String[] infoObtenida){
        for(String[] info:this.informacion){
            if(info[0]==infoObtenida[0]&&info[1]==infoObtenida[1]&&info[2]==infoObtenida[2]&&info[3]==infoObtenida[3]){
                return this.informacion.indexOf(info);
            }
        }
        return 1;
    }
    

